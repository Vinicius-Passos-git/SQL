USE BANK;

SELECT * FROM employee;

SELECT EMP_ID,
	   FNAME,
	   LNAME,
	   START_DATE
FROM EMPLOYEE
WHERE START_DATE BETWEEN '2003-01-01' AND '2007-01-01';

SELECT EMP_ID,
	   FNAME,
	   LNAME,
	   START_DATE
FROM EMPLOYEE
WHERE START_DATE >= '2004-01-01' 
AND START_DATE <='2007-01-01';


SELECT CUST_ID,
	   FED_ID
FROM CUSTOMER 
WHERE cust_type_cd = 'I'
AND FED_ID BETWEEN '500-00-0000' AND '999-99-9999';